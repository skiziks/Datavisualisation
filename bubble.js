function handleBubbleChange() {
    $.ajax({
        url: "./getDataBubble.php",
        type: "GET",
        success: function (dataBubble) {
            var dataJSON = JSON.parse(dataBubble);
            if (dataJSON !== null) {
                bubbleChart.data.datasets[0].data = dataJSON.map(item => ({
                    x: item.pression_arrondie,
                    y: item.humidite_arrondie,
                    r: item.moyenne_temp
                }));

                bubbleChart.update();
            } else {
                console.error("Les données JSON sont nulles.");
            }
        },
        error: function (data) {
            console.log("Erreur" + dataBubble);
        }
    });
}





